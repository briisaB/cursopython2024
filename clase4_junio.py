def es_mayor_de_edad(edad):
    """
    Esta función recibe una edad y retorna True si la edad es mayor o igual a 18,
    de lo contrario, retorna False.
    """
    return edad >= 18

# Prueba de la función con diferentes edades
print(es_mayor_de_edad(20))  # Debería retornar True
print(es_mayor_de_edad(17))  # Debería retornar False
print(es_mayor_de_edad(18))  # Debería retornar True

